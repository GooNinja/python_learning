# Arithmetic Operations
# Python can perform various arithmetic operations just like a calculator. Here are some examples:

# Addition (+): Adds two numbers. Here, 2 + 3 equals 5.
print(2 + 3)   # addition(+)

# Subtraction (-): Subtracts the second number from the first. Here, 3 - 1 equals 2.
print(3 - 1)   # subtraction(-)

# Multiplication (*): Multiplies two numbers. Here, 2 * 3 equals 6.
print(2 * 3)   # multiplication(*)

# Division (/): Divides the first number by the second. Here, 3 / 2 equals 1.5.
print(3 / 2)   # division(/)

# Exponential (**): Raises the first number to the power of the second. Here, 3 ** 2 equals 9.
print(3 ** 2)  # exponential(**)

# Modulus (%): Returns the remainder of the division of the first number by the second. Here, 3 % 2 equals 1.
print(3 % 2)   # modulus(%)

# Floor Division (//): Divides the first number by the second and rounds down to the nearest whole number. Here, 3 // 2 equals 1.
print(3 // 2)  # floor division operator(//)

# Checking Data Types
# In Python, different types of data (like numbers and text) are categorized into various data types. 
# You can check the type of data using the type() function. Here are some examples:

# Int: Represents whole numbers. 10 is an integer.
print(type(10))                  # Int

# Float: Represents decimal numbers. 3.14 is a float.
print(type(3.14))                # Float

# Complex: Represents complex numbers. 1 + 3j is a complex number.
print(type(1 + 3j))              # Complex

# String: Represents text. 'Asabeneh' is a string.
print(type('Asabeneh'))          # String

# List: Represents an ordered collection of items. [1, 2, 3] is a list.
print(type([1, 2, 3]))           # List

# Dictionary: Represents a collection of key-value pairs. {'name':'Asabeneh'} is a dictionary.
print(type({'name':'Asabeneh'})) # Dictionary

# Set: Represents a collection of unique items. {9.8, 3.14, 2.7} is a set.
print(type({9.8, 3.14, 2.7}))    # Set

# Tuple: Represents an ordered collection of items that cannot be changed. (9.8, 3.14, 2.7) is a tuple.
print(type((9.8, 3.14, 2.7)))    # Tuple

