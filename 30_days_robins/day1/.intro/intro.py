print("In total, I have", fruits, "fruits.")

# Different types of data
my_name = "Alice"  # This is a string (text)
my_age = 25        # This is an integer (whole number)integer
my_height = 1.65   # This is a float (decimal number)float

print("My name is", my_name)
print("I am", my_age, "years old")
print("I am", my_height, "meters tall")

# Getting input from the user
user_name = input("What's your name? ")
print("Nice to meet you,", user_name, "!")

# More math
birth_year = input("What year were you born? ")
current_year = 2024  # You might need to update this year
